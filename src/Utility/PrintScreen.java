package Utility;

public class PrintScreen {
    /**
     * All of the methods inside this class is used to print object with bried of System.out.print or System.out.println
     * System.out.printf
     */
    public static void print(Object t) {System.out.print(t);}
    public static void println(Object t) {System.out.println(t);}
    public static void printf(String s, Object t, Object o) {System.out.printf(s,t,o);}
}
